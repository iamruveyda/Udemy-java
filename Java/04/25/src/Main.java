public class Main {

    public static void main(String[] args) {
        /*
        + ---> Toplama Operatörü
        - ---> Çıkarma Operatörü
        / ---> Bölme Operatörü
        * ---> Çarpma Operatörü
        % ---> Kalan Operatörü

        */

        System.out.println(5 + 4.2);
        System.out.println(3 - 5f);
        System.out.println(20 / 4.0);
        System.out.println(4 * 4.5);
        System.out.println(10 % 3);
        System.out.println((3f / 2) + (15 * 5));
        
    }
}
